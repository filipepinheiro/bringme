	/**
	 * This method is used to check if the device is currently connected to the network
	 * @return
	 */
	private boolean isNetworkAvailable(){
		ConnectivityManager mConnManager = (ConnectivityManager) getSystemService(CONNECTIVITY_SERVICE);
		NetworkInfo mNetworkInfo = mConnManager.getActiveNetworkInfo();
		return mNetworkInfo != null && mNetworkInfo.isConnected();
	}
	
	/**
	 * This method is used to check if the device has Google Play Services installed 
	 * @return
	 */
	private boolean isGooglePlayServicesAvailable(){
		int status = GooglePlayServicesUtil.isGooglePlayServicesAvailable(getApplicationContext());
		return (status == ConnectionResult.SUCCESS); 
	}
